export function translateKey(
  locale: string,
  key: string,
  mt?: Record<string, string> | null,
): string {
  const n = normalizeUiLocale(locale) ?? DEFAULT_SITE_LOCALE;
  if (n === "zh-CN") return siteMessages["zh-CN"][key] ?? siteMessages.en[key] ?? key;
  if (typeof mt?.[key] === "string" && mt[key].trim()) return mt[key];
  return siteMessages.en[key] ?? key;
}
