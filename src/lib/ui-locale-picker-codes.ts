/**
 * Canonical language tags for the picker (deduped, sorted).
 * Prefer `Intl.supportedValuesOf("language")` when available (hundreds of tags, incl. many 3-letter).
 */
export function getExtendedUiLocaleCodes(): string[] {
  const acc = new Set<string>();
  try {
    if (typeof Intl !== "undefined" && "supportedValuesOf" in Intl) {
      const list = (Intl as unknown as { supportedValuesOf: (k: string) => string[] }).supportedValuesOf(
        "language",
      );
      for (const code of list) {
        const c = code.trim();
        if (c) acc.add(c);
      }
    }
  } catch {
    /* ignore */
  }
  if (acc.size < 80) {
    for (const c of ISO_639_1_ALPHA2_FALLBACK) acc.add(c);
    for (const c of EXTRA_LOCALE_TAGS) acc.add(c);
  }
  return Array.from(acc).sort((a, b) => a.toLowerCase().localeCompare(b.toLowerCase()));
}
